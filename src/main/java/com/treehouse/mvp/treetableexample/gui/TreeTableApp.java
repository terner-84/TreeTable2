    public TreeTableApp() {
        initComponents();
        
        //Generate items list and root
        ItemsGenerator ig = new ItemsGenerator(100, "-");
        listOfItems = ig.getListOfItems();
        root = ig.getRoot();
        
        //Print items to System Output
        for (Item listOfItem : listOfItems) {
            System.out.println(listOfItem.getJmeno());
            if(listOfItem.getChildren().size() > 0) {
                for (Item child : listOfItem.getChildren()) {
                    System.out.println("   child: " + child.getJmeno());
                }
            }
        }
        
        //TreeTable Item
        TreeTableModelExample tableModel = new TreeTableModelExample(root, listOfItems);
        table.setTreeTableModel(tableModel);
        table.setRowHeight(30);
        table.getColumnModel().getColumn(0).setPreferredWidth(50);
        table.getColumnModel().getColumn(1).setPreferredWidth(300);
        BorderLayout panelLayout = new BorderLayout();
        mainPanel.removeAll();
        mainPanel.setLayout(panelLayout);
        sp.setViewportView(table);
        mainPanel.add(sp);
        
        
        //Tree Item
        ItemTree itt = new ItemTree(root, listOfItems);
        treeItem = itt.getTreeItem();
        BorderLayout treeLayout = new BorderLayout();
        treePanel.removeAll();
        treePanel.setLayout(treeLayout);
        scrollTree.setViewportView(treeItem);
        treePanel.add(scrollTree);
        
        
        setLocationRelativeTo(null);
        
    }

