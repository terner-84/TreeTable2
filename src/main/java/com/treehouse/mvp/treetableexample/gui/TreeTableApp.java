    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        treeItem.getSelectionModel().setSelectionMode(TreeSelectionModel.SINGLE_TREE_SELECTION);
        ItemTree.setExpanded(treeItem);
        treeItem.addTreeSelectionListener(new TreeSelectionListener() {
            @Override
            public void valueChanged(TreeSelectionEvent e) {
                try {
                    Object o = treeItem.getLastSelectedPathComponent();
                DefaultMutableTreeNode node = (DefaultMutableTreeNode)
                        treeItem.getLastSelectedPathComponent();
                textTreeItemListenerOutput.setText(o.toString() 
                            
                            + " e.getPath: " + node.getAllowsChildren());
                } catch (Exception exc) {
                    System.out.println(exc.getMessage());
                }
                
                
                
                            
                    
                
            }
        });
    }//GEN-LAST:event_jButton1ActionPerformed

